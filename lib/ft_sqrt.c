float	ft_sqrt(int nb)
{
	float	cnt;
	float	res;

	res = 0;
	cnt = 1;
	if (nb <= 0)
		return (0);
	while (cnt * cnt < nb * 10000)
	{
		cnt++;
		res++;
	}
	return (res / 100);
}
